            <h3 class="caas_heading">Health and Society Research</h3>


            <p>CAAS  conducts translational research aimed at promoting evidence-based policy and practice for the benefit of patient and population health. Our research is encapsulated by four over-arching, but inter-related, themes which address important societal concerns.</p>

            <p>Our research is underpinned by modern methodological expertise and the key scientific disciplines and also involves close partnership with clinicians, as well as policy makers and practitioners from a range of health and social care settings.  By working closely with the end users of research, we help to ensure that our science is relevant to practice and more likely to be adopted into practice in due course. </p>

            <h4>Applied Epidemiology</h4>
            <p>The Applied Epidemiology research theme contributes to the understanding, prevention and treatment of chronic non-communicable disease across the life course .</p>
            <img src="img/health.png" class="img-responsive pull-right" width="300" height="300" style="margin: 10px 0px 10px 10px; border: 1px solid #ccc;"/>

            <p>Our work is multidisciplinary engaging with colleagues in public health, clinical medicine, sociology, anthropology, health economics, medical physics, statistics, epigenetics and with the public. We adopt a broad range of methodological approaches including: epidemiology; demographic methods; modelling disease trends; systematic reviews; genetic epidemiology; spatio-temporal analyses; and longitudinal and intervention studies. We also strive to build research capacity and to inform national and international policy.</p>

            <h4>Public Health Improvement</h4>
            <p>Public Health Improvement (PHI) is a multidisciplinary research theme that brings together health, social and behavioral science and professional expertise to conduct applied health research with a focus on the development, evaluation and translation of health interventions, as well as on related theory and methods. </p>
            <p>Our research is built around five broad programme : </p>

            <ul>
                <li >Understanding and tackling health inequalities </li>
                <li >Understanding and changing health related behaviors </li>
                <li >Evaluation of public health policy </li>
                <li >Public health nutrition</li>
                <li >Prevention of alcohol misuse </li>

            </ul>

            <p>There is considerable overlap and inter-disciplinary working between these programme. </p>

            <h4>Decision Making and Organization of Care</h4>


            <p>Decision Making and Organization of Care (DMOC) is a multidisciplinary research theme which brings together social and behavioral science and clinical expertise to conduct applied health research. DMOC incorporates three main complementary areas of work: </p>


            <ul>
                <li >Implementation/improvement science  </li>
                <li >Shared decision making/patient centered care  </li>
                <li >Experience of health, illness and healthcare </li>

            </ul>


            <p>Our work spans a diverse range of clinical specialties and utilizes a range of research methods and methodologies, employing rigorous single and multi-method qualitative and quantitative research, as well as strategically employing surveys, interactional analysis and systematic reviews.</p>
            <h4>Life-Course, Development and Ageing</h4>
            <p>Life course, Development and Ageing (LCDA) is a multidisciplinary theme which brings together a wide range of academics and clinicians to conduct applied health research across the lifespan. The group includes academic expertise in mixed methods research with representation from health psychology, medical sociology and social gerontology and clinical expertise from both primary and secondary care. Our research explores the whole spectrum of healthy ageing, from development in childhood and the consequences of disability, to the health and wellbeing of the oldest old, those aged 85 years and over.  </p>
            <p>CAAS research theme as follows: </p>



            <ul>
                <li >Ageing and Health</li>
                <li >Transitions in health and disability</li>
                <li >Understanding child disability</li>
                <li >Improving quality of life</li>
                <li >Technologies and the environment</li>
                <li >Cognitive impairment and dementia.</li>

            </ul>

            <h4>Research and furthering understanding</h4>
            <p>Furthering understanding of all areas of health and society is a key objective of the CAAS and we conduct research of international importance that helps to shape our lives – covering everything from experience of hospitalization through to the work of our voluntary Association for Government organization and some of the Helping groups. This research conducted by research scholars in medical field . </p>
            <h4>Research impact</h4>
            <p>The Institute’s transformational research projects seek to address some of the major issues both within our professions, local services, our community and beyond it. By pulling together academics working across disciplines to tackle important, social, scientific and environmental challenges, these projects are making an impact on people's lives and helping to expand our base of world-leading research. </p>
