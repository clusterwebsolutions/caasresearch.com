            <p>Again, it has been widely recognized in the literature that not only do many Industrial Media journalists fail to take into account the margin of error when assessing ‘leads’ in point estimates, but also that those who do recognize its importance often fail to appreciate that the potential impact of sampling error must be taken into account for each point estimate. As such, the margin of error on the difference in support between two candidates is actually twice the sampling error for the poll in question.</p>

            <p>A further issue of statistical interpretation relevant to the coverage of polling data that emerges from the literature is the treatment of poll results that are ‘outliers’ from the general trend of the data. An often neglected aspect of discussions of sampling error in media coverage is the fact that these margins of error are generally only calculated at a 95% confidence interval. This means the point estimates returned by a poll are likely to fall within the margin of error of the actual level support within the population 95% of the time. However, one out of twenty polls will return a point estimate outside even the margin of error . In light of this, caution should be exercised when drawing inferences from a poll that includes produces a point estimate that seems to deviate substantially from the long-term trend. </p>

