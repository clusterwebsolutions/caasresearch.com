            <h3 class="caas_heading">Intellectual Property Rights</h3>

            <img src="img/intellectual_property.png" class="img-responsive pull-right" width="300" height="300" style="margin: 10px 0px 10px 10px; border: 1px solid #ccc;"/>


            <p>Intellectual property (IP) refers to creations of the mind, such as inventions; literary and artistic works; designs; and symbols, names and images used in commerce.</p>

            <p>IP is protected in law by, for example, patents, copyright and trademarks, which enable people to earn recognition or financial benefit from what they invent or create. By striking the right balance between the interests of innovators and the wider public interest, the IP system aims to foster an environment in which creativity and innovation can flourish.</p>

            <h4>(i) Copyright and rights related to copyright. </h4>

            <p>The rights of authors of literary and artistic works (such as books and other writings, musical compositions, paintings, sculpture, computer programs and films) are protected by copyright, for a minimum period of 50 years after the death of the author.</p>

            <p>Also protected through copyright and related (sometimes referred to as “neighbouring”) rights are the rights of performers (e.g. actors, singers and musicians), producers of phonograms (sound recordings) and broadcasting organizations. The main social purpose of protection of copyright and related rights is to encourage and reward creative work.</p>

            <h4>(ii) Industrial property. </h4>
            <h5>Industrial property can usefully be divided into two main areas:</h5>
            <ul>
                <li >One area can be characterized as the protection of distinctive signs, in particular trademarks (which distinguish the goods or services of one undertaking from those of other undertakings) and geographical indications (which identify a good as originating in a place where a given characteristic of the good is essentially attributable to its geographical origin).</li>
                <li >The protection of such distinctive signs aims to stimulate and ensure fair competition and to protect consumers, by enabling them to make informed choices between various goods and services. The protection may last indefinitely, provided the sign in question continues to be distinctive.</li>
                <li >Other types of industrial property are protected primarily to stimulate innovation, design and the creation of technology. In this category fall inventions (protected by patents), industrial designs and trade secrets.</li>
                <li >The social purpose is to provide protection for the results of investment in the development of new technology, thus giving the incentive and means to finance research and development activities.</li>
                <li >A functioning intellectual property regime should also facilitate the transfer of technology in the form of foreign direct investment, joint ventures and licensing.</li>

            </ul>



            <p>The protection is usually given for a finite term (typically 20 years in the case of patents).
                While the basic social objectives of intellectual property protection are as outlined above, it should also be noted that the exclusive rights given are generally subject to a number of limitations and exceptions, aimed at fine-tuning the balance that has to be found between the legitimate interests of right holders and of users.</p>
            <h3 class="caas_heading">Copyright</h3>
            <h4>What is copyright?</h4>
            <p>Copyright (or author’s right) is a legal term used to describe the rights that creators have over their literary and artistic works. Works covered by copyright range from books, music, paintings, sculpture, and films, to computer programs, databases, advertisements, maps, and technical drawings.</p>
            <h3 class="caas_heading">What can be protected using copyright</h3>
            <p>Exhaustive lists of works covered by copyright are usually not to be found in legislation. Nonetheless, broadly speaking, works commonly protected by copyright throughout the world include:</p>

            <ul>
                <li>literary works such as novels, poems, plays, reference works, newspaper articles;</li>
                <li>computer programs, databases;</li>
                <li>films, musical compositions, and choreography;</li>
                <li>artistic works such as paintings, drawings, photographs, and sculpture;</li>
                <li>architecture; and</li>
                <li>Advertisements, maps, and technical drawings.</li>
            </ul>

            <p>Copyright protection extends only to expressions, and not to ideas, procedures, methods of operation or mathematical concepts as such. Copyright may or may not be available for a number of objects such as titles, slogans, or logos, depending on whether they contain sufficient authorship.</p>

            <h4>What rights does copyright give me? What are my rights as author of a work? </h4>
            <p>There are two types of rights under copyright:</p>
            <ul>
                <li>economic rights, which allow the rights owner to derive financial reward from the use of his works by others; and</li>
                <li>moral rights, which protect the non-economic interests of the author.</li>
            </ul>

            <p>Most copyright laws state that the rights owner has the economic right to authorize or prevent certain uses in relation to a work or, in some cases, to receive remuneration for the use of his work (such as through collective management). The economic rights owner of a work can prohibit or authorize:</p>
            <ul>
                <li>its reproduction in various forms, such as printed publication or sound recording;</li>
                <li>its public performance, such as in a play or musical work;</li>
                <li>its recording, for example, in the form of compact discs or DVDs;</li>
                <li>its broadcasting, by radio, cable or satellite;</li>
                <li>its translation into other languages; and</li>
                <li>its adaptation, such as a novel into a film screenplay.</li>
            </ul>
            <p>Examples of widely recognized moral rights include the right to claim authorship of a work and the right to oppose changes to a work that could harm the creator's reputation.</p>

            <h4>Can I register copyright? </h4>
            <p> In the majority of countries, and according to the Berne Convention, copyright protection is obtained automatically without the need for registration or other formalities.</p>
            <p>Most countries nonetheless have a system in place to allow for the voluntary registration of works. Such voluntary registration systems can help solve disputes over ownership or creation, as well as facilitate financial transactions, sales, and the assignment and/or transfer of rights.</p>
            <p>Please note that WIPO does not offer a copyright registration system or a searchable copyright database</p>

            <h3 class="caas_heading">Patent </h3>
            <p>A patent is an exclusive right granted for an invention, which is a product or a process that provides, in general, a new way of doing something, or offers a new technical solution to a problem. To get a patent, technical information about the invention must be disclosed to the public in a patent application.</p>

            <h4>What kind of protection does a patent offer? </h4>
            <p>In principle, the patent owner has the exclusive right to prevent or stop others from commercially exploiting the patented invention. In other words, patent protection means that the invention cannot be commercially made, used, distributed, imported or sold by others without the patent owner's consent.</p>
            <h4>Is a patent valid in every country? </h4>
            <p>Patents are territorial rights. In general, the exclusive rights are only applicable in the country or region in which a patent has been filed and granted, in accordance with the law of that country or region.</p>
            <h4>How long does a patent last? </h4>
            <p>The protection is granted for a limited period, generally 20 years from the filing date of the application.</p>
            <h3 class="caas_heading">Trademarks</h3>
            <p>A trademark is a sign capable of distinguishing the goods or services of one enterprise from those of other enterprises. Trademarks date back to ancient times when craftsmen used to put their signature or "mark" on their products.</p>
            <h4>What is an industrial design?</h4>
            <p>In a legal sense, an industrial design constitutes the ornamental or aesthetic aspect of an article.
                An industrial design may consist of three dimensional features, such as the shape of an article, or two dimensional features, such as patterns, lines or color.</p>
            <h4>What kind of protection does an industrial design right offer? </h4>
            <p>In principle, the owner of a registered industrial design or of a design patent has the right to prevent third parties from making, selling or importing articles bearing or embodying a design which is a copy, or substantially a copy, of the protected design, when such acts are undertaken for commercial purposes.</p>
            <h4>What kind of products can benefit from industrial design protection? </h4>
            <p>Industrial designs are applied to a wide variety of products of industry and handicraft items: from packages and containers to furnishing and household goods, from lighting equipment to jewelry, and from electronic devices to textiles. Industrial designs may also be relevant to graphic symbols, graphical user interfaces (GUI), and logos.</p>
            <h4>How are industrial designs protected? </h4>
            <p>In most countries, an industrial design needs to be registered in order to be protected under industrial design law as a “registered design”. In some countries, industrial designs are protected under patent law as “design patents ”.</p>
            <p>Industrial design laws in some countries grant – without registration – time- and scope limited protection to so-called “unregistered industrial designs”.</p>
            <p>Depending on the particular national law and the kind of design, industrial designs may also be protected as works of art under copyright law.</p>
            <h4>What is a geographical indication?</h4>
            <p>A geographical indication (GI) is a sign used on products that have a specific geographical origin and possess qualities or a reputation that are due to that origin. In order to function as a GI, a sign must identify a product as originating in a given place. In addition, the qualities, characteristics or reputation of the product should be essentially due to the place of origin. Since the qualities depend on the geographical place of production, there is a clear link between the product and its original place of production.</p>
            <h4>What rights does a geographical indication provide? </h4>
            <p>A geographical indication right enables those who have the right to use the indication to prevent its use by a third party whose product does not conform to the applicable standards. For example, in the jurisdictions in which the Darjeeling geographical indication is protected, producers of Darjeeling tea can exclude use of the term “Darjeeling” for tea not grown in their tea gardens or not produced according to the standards set out in the code of practice for the geographical indication.</p>
            <p>However, a protected geographical indication does not enable the holder to prevent someone from making a product using the same techniques as those set out in the standards for that indication. Protection for a geographical indication is usually obtained by acquiring a right over the sign that constitutes the indication.</p>
            <h4>For what type of products can geographical indications be used? </h4>
            <p>Geographical indications are typically used for agricultural products, foodstuffs, wine and spirit drinks, handicrafts, and industrial products.</p>
            <h4>How are geographical indications protected? </h4>
            <p> There are three main ways to protect a geographical indication:</p>
            <ul>
                <li>so-called sui generis systems (i.e. special regimes of protection);</li>
                <li>using collective or certification marks; and</li>
                <li>methods focusing on business practices, including administrative product approval schemes.</li>
            </ul>
            <p>These approaches involve differences with respect to important questions, such as the conditions for protection or the scope of protection. On the other hand, two of the modes of protection — namely sui generis systems and collective or certification mark systems — share some common features, such as the fact that they set up rights for collective use by those who comply with defined standards.</p>
            <p>Broadly speaking geographical indications are protected in different countries and regional systems through a wide variety of approaches and often using a combination of two or more of the approaches outlined above. These approaches have been developed in accordance with different legal traditions and within a framework of individual historical and economic conditions.</p>
